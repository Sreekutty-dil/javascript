// operators
/*
                 Arithmetic operators :
    // Arithmetic operators are used to perform arithemetic on numners.
    // 1. Addition (+)
    // 2. Subtraction (-)
    // 3. Multiplication (*)
    // 4. Division (/)
    // 5. Exponentiation (**)
    // 6. Remainder (%)
    // 7. Increment (++)
    // 8. Decrement (--)
*/
var a = 10;
var b = 20;
document.write(a + b); // Addition operator
document.write(`<br>`);
document.write(a - b); // Subtraction operator
document.write(`<br>`);
document.write(a * b); // Multiply operator
document.write(`<br>`);
document.write(a / b); // Divide operator
document.write(`<br>`);
document.write(a % b); // Modulus operator
document.write(`<br>`);

 // increment/Decrement operators
var a = 10;
a++;
document.write(a); // increment
 document.write(`<br>`);
 ++a;
 document.write(a);
document.write(`<br>`);
var a = 10;
a--;
document.write(a); // decrement

// Explanation operator (power series operators)
var a = 10;
document.write(`<br>`);
document.write(a ** 2); // Square of a
document.write(`<br>`);
 document.write(a ** 3); // Cube of a
