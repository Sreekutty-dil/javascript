// for loop
for(var i =1;i<=5;i++) {
    document.write("Hello" + "<br>");
}


document.write("<br>");


// while loop
var i= 1;
while(i<=5) {
    document.write(i + "<br>");
    i++
}

document.write("<br>");

//do while loop
var i = 1;
do {
    document.write(i + "<br>");
    i++
}while(i<=5);






