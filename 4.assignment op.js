// Assignment operators :
 /* the assignment operator (=) assigns a value to a variable. 
  operator         example       same as
  =                 x = y        x = y  // assignment operator
  +=                x += y       x = x + y // add and assign
  -=                x -= y       x = x - y // subtract and assign
  *=                x *= y       x = x * y // multiply and assign
  /=                x /= y       x = x / y // divide and assign
  %=                x %= y       x = x % y // modules and assign
  **=               x **= y       x = x ** y // exponentiation and assignment
 */
var x = 10;
document.write(x); // assignment operator
